#include "builtin.h"

void	ft_pwd(void)
{
	char	*cwd;

	cwd = getcwd(NULL, 0);
	if (!cwd)
		ft_perror("pwd");
	ft_putstr_fd(cwd, 1);
	ft_putchar_fd('\n', 1);
	ft_free(cwd);
}
