#include "builtin.h"

void	ft_pwd(t_info *info)
{
	char	*cwd;

	cwd = getcwd(NULL, 0);
	if (!cwd)
	{
		info->status = 1;
		perror("pwd");
	}
	ft_putstr_fd(cwd, 1);
	ft_putchar_fd('\n', 1);
	free(cwd);
}
